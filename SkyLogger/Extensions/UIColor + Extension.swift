import UIKit
import Hue

extension UIColor {
    
    public static var background: UIColor {
        return UIColor(hex: "#06001D")
    }
    
    public static var backgroundLight: UIColor {
        return UIColor(hex: "#FAFAFA")
    }
    
    public static var textSecondary: UIColor {
        return UIColor(hex: "#AFAFAF")
    }
    
}
